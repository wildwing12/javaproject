package ch02;

public class RealTypeEx {
	public static void main(String[] args) {
		double d = 0.1234;
		double e = 1234E-4;//이거는 지수 형태, 1234E-4
		float f = 0.1234f;//float형은 숫자 뒤 F또는  f를 붙임
		double w = .1234D;//D는 생략가능, .1234D와 .1234와 0.1234는 같다
		
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(w);
		
}
}
